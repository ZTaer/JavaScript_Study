const useStyles = makeStyles((theme) => ({
  root: {
    width: '100%',
  },
  paper: {
    width: '100%',
    marginBottom: theme.spacing(2),
  },
  table: {
    minWidth: 750,
  },
  visuallyHidden: {
    border: 0,
    clip: 'rect(0 0 0 0)',
    height: 1,
    margin: -1,
    overflow: 'hidden',
    padding: 0,
    position: 'absolute',
    top: 20,
    width: 1,
  },
}));

export default function EnhancedTable() {
  const classes = useStyles();
  const [order, setOrder] = React.useState('asc');
  const [orderBy, setOrderBy] = React.useState('calories');
  const [selected, setSelected] = React.useState([]);
  const [page, setPage] = React.useState(0);
  const [dense, setDense] = React.useState(false);
  const [rowsPerPage, setRowsPerPage] = React.useState(5);

  const handleRequestSort = (event, property) => {
    const isAsc = orderBy === property && order === 'asc';
    setOrder(isAsc ? 'desc' : 'asc');
    setOrderBy(property);
  };

  const handleSelectAllClick = (event) => {
    if (event.target.checked) {
      const newSelecteds = rows.map((n) => n.name);
      setSelected(newSelecteds);
      return;
    }
    setSelected([]);
  };

  /**
   * 追踪 - 5( 等待笔记 )
   * a) 选中元素加工至selected核心逻辑
   *    0. name不存在: 添加元素至selected
   *    1. name已存在: 删除元素至selected
   * b) 参数: event为强制参数, name为待加工值
   * c) Array.indexOf( target ): 索引数组中指定目标，不存在则返回-1，存在则返回索引值
   * d) Array.concat( xxx ): 数组合并
   * e) Array.slice(): 获取数组切片
   *    0. Array.slice( 开始值, 结束值 ): 获取数组片段
   *    1. Array.slice( 值 ): 只获取'值后'的数组元素
   */
  const handleClick = (event, name) => {
    const selectedIndex = selected.indexOf(name);     // 0. 验证元素是否已经存在selected中
    let newSelected = [];

    if (selectedIndex === -1) {                       // 1. 不存在: 元素不在selected时, 元素name添加至selected
      newSelected = newSelected.concat(selected, name);
    } 
    else if (selectedIndex === 0) {                   // 2. 存在: 开头位置时, 则只获取除开头位置之后的元素覆盖selected
      newSelected = newSelected.concat(selected.slice(1));
    } 
    else if (selectedIndex === selected.length - 1) { // 3. 存在: 结尾位置时, 则只获取除结尾位置之前的元素覆盖selected
      newSelected = newSelected.concat(selected.slice(0, -1));
    } 
    else if (selectedIndex > 0) {                     // 4. 存在: 非开头/结尾位置时, 则利用slice二段截取合并的方式排除目标元素，在进行覆盖selected
      newSelected = newSelected.concat(
        selected.slice(0, selectedIndex),
        selected.slice(selectedIndex + 1),
      );
    }

    setSelected(newSelected);
  };

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  const handleChangeDense = (event) => {
    setDense(event.target.checked);
  };

  /**
   * 追踪 - 1
   * 验证name是否存在selected数组中, 存在返回true，不存在返回false
   */
  const isSelected = (name) => selected.indexOf(name) !== -1; // Array.indexOf指定数据不存在数组中，返回-1

  const emptyRows = rowsPerPage - Math.min(rowsPerPage, rows.length - page * rowsPerPage);

  return (
    <div className={classes.root}>
      <Paper className={classes.paper}>
        <EnhancedTableToolbar numSelected={selected.length} />
        <TableContainer>
          <Table
            className={classes.table}
            aria-labelledby="tableTitle"
            size={dense ? 'small' : 'medium'}
            aria-label="enhanced table"
          >
            <EnhancedTableHead
              classes={classes}
              numSelected={selected.length}
              order={order}
              orderBy={orderBy}
              onSelectAllClick={handleSelectAllClick} // 决定是否全部勾选
              onRequestSort={handleRequestSort}
              rowCount={rows.length}
            />
            <TableBody>
              {stableSort(rows, getComparator(order, orderBy))
                .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                .map((row, index) => {

                  /**
                   * 追踪 - 0
                   * 通过isItemSelected来决定是否勾选, true为勾选, false为不勾选
                   */
                  const isItemSelected = isSelected(row.name);
                  // const isItemSelected = true;

                  const labelId = `enhanced-table-checkbox-${index}`;

                  return (
                    <TableRow
                      hover
                      onClick={(event) => handleClick(event, row.name)} // 追踪 - 4 name添加到selected数组核心函数( selected保存着选中元素的name )
                      role="checkbox"
                      aria-checked={isItemSelected}
                      tabIndex={-1}
                      key={row.name}
                      selected={isItemSelected}     // 追踪 - 3 selected属性: 为true时有选中背景色
                    >
                      <TableCell padding="checkbox">
                        <Checkbox
                          /**
                           * 追踪 - 2 
                           * 通过checked来反应是否进行勾选
                           */
                          checked={isItemSelected}
                          inputProps={{ 'aria-labelledby': labelId }}
                        />
                      </TableCell>
                      <TableCell component="th" id={labelId} scope="row" padding="none">
                        {row.name}
                      </TableCell>
                      <TableCell align="right">{row.calories}</TableCell>
                      <TableCell align="right">{row.fat}</TableCell>
                      <TableCell align="right">{row.carbs}</TableCell>
                      <TableCell align="right">{row.protein}</TableCell>
                    </TableRow>
                  );
                })}
              {emptyRows > 0 && (
                <TableRow style={{ height: (dense ? 33 : 53) * emptyRows }}>
                  <TableCell colSpan={6} />
                </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
        <TablePagination
          rowsPerPageOptions={[5, 10, 25]}
          component="div"
          count={rows.length}
          rowsPerPage={rowsPerPage}
          page={page}
          onChangePage={handleChangePage}
          onChangeRowsPerPage={handleChangeRowsPerPage}
        />
      </Paper>
      <FormControlLabel
        control={<Switch checked={dense} onChange={handleChangeDense} />}
        label="Dense padding"
      />
    </div>
  );
}
